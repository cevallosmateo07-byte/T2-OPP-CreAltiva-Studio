    private void btnScheduleVideoCallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScheduleVideoCallActionPerformed
        
        int row = tblVideoCalls.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente");
            return;
        }

        int customerId = Integer.parseInt(
                tblVideoCalls.getValueAt(row, 0).toString()
        );

        Date selectedDate = dcVideoCallDate.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Seleccione una fecha");
            return;
        }

        String date = new SimpleDateFormat("yyyy-MM-dd").format(selectedDate);
        String hour = txtHour.getText().trim();
        String medium = cmbMedium.getSelectedItem().toString();

        String result = VideoCallController.scheduleVideoCall(
                customerId,
                date,
                hour,
                medium
        );

        if (!"OK".equals(result)) {
            JOptionPane.showMessageDialog(this, result);
            return;
        }

        loadVideoCallsTable();

        JOptionPane.showMessageDialog(this, "Videollamada registrada correctamente");

    }//GEN-LAST:event_btnScheduleVideoCallActionPerformed

