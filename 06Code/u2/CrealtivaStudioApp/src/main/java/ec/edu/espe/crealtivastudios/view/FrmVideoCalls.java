    private void loadVideoCallsTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblVideoCalls.getModel();
        model.setRowCount(0);

        List<Document> calls = CrudOperations.findAll("videocalls");
        List<Customer> customers = CustomerController.getAllCustomersUnified();

        for (Customer customer : customers) {
            Document callDoc = calls.stream()
                    .filter(c -> c.getInteger("customerId") == customer.getId())
                    .findFirst()
                    .orElse(null);

            if (callDoc != null) {
                model.addRow(new Object[]{
                    customer.getId(),
                    customer.getName(),
                    callDoc.getString("datetime"),
                    callDoc.getString("medium")
                });
            } else {
                model.addRow(new Object[]{
                    customer.getId(),
                    customer.getName(),
                    "",
                    ""
                });
            }
        }
    }


