private void loadBillingData() {
    try {
        MongoDatabase db = MongoDBConnection.getConnection();
        MongoCollection<Document> collection = db.getCollection("PhotographerDB");

        tableModel.setRowCount(0);

        for (Document doc : collection.find()) {

            String name = doc.getString("name");
            String event = doc.getString("assignedEvent");
            if (event == null || event.isEmpty()) event = "SIN EVENTO";

            String paymentStatus = doc.getString("paymentStatus");
            if (paymentStatus == null) paymentStatus = "PENDIENTE";

            String equipment = "Ninguno";
            try {
                java.util.List<String> eqList = (java.util.List<String>) doc.get("equipment");
                if (eqList != null && !eqList.isEmpty()) {
                    equipment = String.join(", ", eqList);
                }
            } catch (Exception e) {}

            tableModel.addRow(new Object[]{
                name,
                event,
                equipment,
                paymentStatus
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error cargando datos: " + e.getMessage());
    }
}

