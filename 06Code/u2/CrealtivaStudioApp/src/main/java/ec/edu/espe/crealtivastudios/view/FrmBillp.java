    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla");
        return;
    }

    String photographerName = jTable1.getValueAt(selectedRow, 0).toString();
    String assignedEvent = jTable1.getValueAt(selectedRow, 1).toString();
    String newStatus = jComboBox1.getSelectedItem().toString();

    // ðŸ”’ VALIDACIÃ“N: EVENTO OBLIGATORIO
    if (assignedEvent.equalsIgnoreCase("SIN EVENTO")) {
        JOptionPane.showMessageDialog(
            this,
            "Primero asigne un evento",
            "AcciÃ³n no permitida",
            JOptionPane.WARNING_MESSAGE
        );
        return;
    }

    try {
        MongoDatabase db = MongoDBConnection.getConnection();
        MongoCollection<Document> collection = db.getCollection("PhotographerDB");

        collection.updateOne(
            eq("name", photographerName),
            new Document("$set", new Document("paymentStatus", newStatus))
        );

        JOptionPane.showMessageDialog(this, "Estado actualizado correctamente");
        loadBillingData();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

