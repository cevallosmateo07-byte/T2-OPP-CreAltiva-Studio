    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
                                        
        if (!validateForm()) {
            return; // si la validación falla, no guarda
        }

        // LEER LOS DATOS AQUÍ:
        String name = txtCustomerName.getText();
        String hour = ffVideoCallHour.getText();
        Date date = ffVideoCallDate.getDate();
        String medium = cbMedium.getSelectedItem().toString();
        String note = txtNote.getText();

        // CREAR EL OBJETO AQUÍ:
        VideoCall call = new VideoCall();
        call.setCostumerName(name);
        call.setVideoCallHour(hour);
        call.setVideoCallDate(formatDate(date));
        call.setMedium(medium);
        call.setNote(note);

        JOptionPane.showMessageDialog(
            this,
            "Video call guardada con éxito\n" + call.toString()
        );
        
    }//GEN-LAST:event_btnSaveActionPerformed
    private boolean validateForm() {
        String name = txtCustomerName.getText();
        String hour = ffVideoCallHour.getText();
        Date date = ffVideoCallDate.getDate();

        if (!validateName(name)) {
            JOptionPane.showMessageDialog(this, "Nombre inválido");
            return false;
        }

        if (!validateHour(hour)) {
            JOptionPane.showMessageDialog(this, "Hora inválida (use HH:mm)");
            return false;
        }

        if (!validateDate(date)) {
            JOptionPane.showMessageDialog(this, "Fecha inválida");
            return false;
        }

        int option = JOptionPane.showConfirmDialog(
            this,
            "¿Está seguro de que desea guardar?",
            "Confirmar guardado",
            JOptionPane.YES_NO_CANCEL_OPTION,
            JOptionPane.QUESTION_MESSAGE
        );

        if (option != JOptionPane.YES_OPTION) {
            return false; // canceló o no quiere guardar
        }

        return true;
    }

     
