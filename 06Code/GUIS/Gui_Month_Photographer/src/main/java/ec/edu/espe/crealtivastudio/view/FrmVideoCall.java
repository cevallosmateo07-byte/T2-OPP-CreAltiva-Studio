    private boolean validateDate(Date date) {
        
        if (date == null) {
            System.out.println("La fecha está vacía");
            return false;
        }

        Date today = new Date();

        // Eliminamos horas para comparar correctamente solo la fecha
        if (date.before(today)) {
            System.out.println("La fecha es pasada");
            return false;
        }

        return true;
    }
    
    
    private String formatDate(Date date) {
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        return sdf.format(date);
    }

    
    private boolean validateHour(String hour) {
        if (hour == null || hour.trim().isEmpty()) {
            System.out.println("La hora está vacía");
            return false;
        }

        if (!hour.matches("^([01]\\d|2[0-3]):[0-5]\\d$")) {
            System.out.println("La hora no tiene un formato correcto (HH:mm)");
            return false;
        }

        return true;
    }
    
    
