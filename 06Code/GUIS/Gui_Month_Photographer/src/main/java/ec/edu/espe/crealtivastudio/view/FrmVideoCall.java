    private boolean validateName(String name) {
        if (name == null || name.trim().isEmpty()) {
            System.out.println("El nombre está vacío");
            return false;
        }

        if (!name.matches("[a-zA-Z ]+")) {
            System.out.println("El nombre solo debe contener letras");
            return false;
        }

        return true;
    }
    
