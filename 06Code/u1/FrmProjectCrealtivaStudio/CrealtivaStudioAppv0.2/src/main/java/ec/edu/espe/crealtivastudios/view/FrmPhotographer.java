    /**
     * @param args the command line arguments
     */
    private void addToListOnce(String name) {
    // Verificar duplicados usando el texto generado por assignPhotographer
    for (int i = 0; i < listAssignedModel.size(); i++) {
        if (listAssignedModel.get(i).contains(name)) {
            return; // ya existe
        }
    }

    // SOLO UNA VEZ se agrega desde assignPhotographer
    assignPhotographer(name);
}

