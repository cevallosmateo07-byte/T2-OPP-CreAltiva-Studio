    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        String result;

        if (editing) {
            result = CustomerController.updateCustomer(
                editingCustomerId,
                txtName.getText(),
                txtPhone.getText(),
                txtEmail.getText(),
                txtAddress.getText()
            );
        } else {
            result = CustomerController.registerCustomer(
                txtName.getText(),
                txtPhone.getText(),
                txtEmail.getText(),
                txtAddress.getText()
            );
        }

        if ("OK".equals(result)) {
            JOptionPane.showMessageDialog(this, "Operacion realizada correctamente");
            clearForm();
            loadCustomersTable();
            editing = false;
            editingCustomerId = -1;
            btnRegister.setText("Registrar");
        } else {
            JOptionPane.showMessageDialog(this, result);
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnEditCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditCustomerActionPerformed
        
        int row = tblCustomers.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla");
            return;
        }

        editingCustomerId = Integer.parseInt(
            tblCustomers.getValueAt(row, 0).toString()
        );

        txtName.setText(tblCustomers.getValueAt(row, 1).toString());
        txtPhone.setText(tblCustomers.getValueAt(row, 2).toString());
        txtEmail.setText(tblCustomers.getValueAt(row, 3).toString());
        txtAddress.setText(tblCustomers.getValueAt(row, 4).toString());

        editing = true;
        btnRegister.setText("Actualizar");
        
    }//GEN-LAST:event_btnEditCustomerActionPerformed

    private void btnDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCustomerActionPerformed
            int row = tblCustomers.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un cliente");
        return;
    }

    int id = Integer.parseInt(
            tblCustomers.getValueAt(row, 0).toString()
    );

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Â¿Desea eliminar el cliente?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    String result = CustomerController.deleteCustomer(id);

    if ("OK".equals(result)) {
        JOptionPane.showMessageDialog(this, "Cliente eliminado");
        loadCustomersTable();
    } else {
        JOptionPane.showMessageDialog(this, result);
    }

    }//GEN-LAST:event_btnDeleteCustomerActionPerformed
    
    
    private void loadCustomersTable() {

        String[] columns = {"ID", "Nombre", "Telefono", "Email", "Direccion"};

        DefaultTableModel model = new DefaultTableModel(columns, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Customer c : CustomerController.getAllCustomersUnified()) {
            model.addRow(new Object[]{
                c.getId(),
                c.getName(),
                c.getPhone(),
                c.getEmail(),
                c.getAddress()
            });
        }

        tblCustomers.setModel(model);
    }

    private void clearForm() {
    txtName.setText("");
    txtPhone.setText("");
    txtEmail.setText("");
    txtAddress.setText("");
    }
    

    
