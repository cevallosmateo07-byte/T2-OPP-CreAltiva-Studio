    private void btnAssignAndSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignAndSaveActionPerformed
        String eventName = txtEventName.getText();
        Date date = cldDateChooser.getDate();
        int disccount = (int) spnDisccount.getValue();
        String evenType = (String) cmbEventType.getSelectedItem();

        if (!FrameValidations.validateEventName(eventName)) {
            JOptionPane.showMessageDialog(this, "El nombre del evento debe tener al menos 3 caracteres");
            return;
        }

        if (date == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fecha");
            return;
        }

        int eventId = generateEventId();

        Document event = new Document("eventId", eventId)
                .append("eventName", eventName)
                .append("date", date)
                .append("discount", disccount)
                .append("eventType", evenType);

        CrudOperations.insert("Events", event);
        JOptionPane.showMessageDialog(this, "Evento guardado en la base de datos Mongo DB");

        loadEventsIntoTextArea();

        txtEventName.setText("");
        spnDisccount.setValue(0);
        cldDateChooser.setDate(null);
    }//GEN-LAST:event_btnAssignAndSaveActionPerformed

