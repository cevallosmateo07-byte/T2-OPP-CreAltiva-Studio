    private int generateEventId() {
        try {
            MongoDatabase database = MongoDBConnection.getConnection();
            MongoCollection<Document> eventsCollection = database.getCollection("Events");

            Document maxIdDoc = eventsCollection.find()
                    .sort(new Document("eventId", -1))
                    .limit(1)
                    .first();

            if (maxIdDoc != null && maxIdDoc.containsKey("eventId")) {
                return maxIdDoc.getInteger("eventId") + 1;
            } else {
                return 1; 
            }
        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Error al generar ID", e);
            return 1;
        }
    }

    private void loadEventsIntoTextArea() {
        try {
            txaShowEvents.setText("");

            MongoDatabase database = MongoDBConnection.getConnection();
            MongoCollection<Document> eventsCollection = database.getCollection("Events");

            FindIterable<Document> events = eventsCollection.find().sort(new Document("eventId", -1));

            for (Document event : events) {
                int eventId = event.getInteger("eventId", 0);
                String eventName = event.getString("eventName");
                Date date = event.getDate("date");
                int discount = event.getInteger("discount", 0);
                String eventType = event.getString("eventType");

                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                String formattedDate = (date != null) ? sdf.format(date) : "Sin fecha";

                String eventInfo = String.format(
                        "ID: %d\nEvento: %s\nTipo: %s\nFecha: %s\nDescuento: %d%%\n%s\n\n",
                        eventId, eventName, eventType, formattedDate, discount, "--------------------------------"
                );

                txaShowEvents.append(eventInfo);
            }

            if (!events.iterator().hasNext()) {
                txaShowEvents.setText("No hay eventos registrados");
            }
        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Error al cargar eventos", e);
            JOptionPane.showMessageDialog(this, "Error al cargar eventos: " + e.getMessage());

        }
    }

