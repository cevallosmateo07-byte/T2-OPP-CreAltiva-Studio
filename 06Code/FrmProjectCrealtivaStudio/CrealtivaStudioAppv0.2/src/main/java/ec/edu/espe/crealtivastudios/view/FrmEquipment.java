    private void toggleEquipmentStatus() {
        // 1. Verificar selección
        int index = listEquipment.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un equipo de la lista para cambiar su estado.");
            return;
        }

        // 2. Obtener el texto y extraer el ID
        String selectedString = listEquipmentModel.get(index);
        try {
            String idPart = selectedString.split("\\|")[0]; 
            int id = Integer.parseInt(idPart.replace("ID:", "").trim());

            // 3. Buscar el objeto real en el Modelo
            Equipment eq = Equipment.findById(id);

            if (eq != null) {
                // 4. Lógica de Switch (Interruptor)
                if (eq.isAvailable()) {
                    eq.markAsInUse(); // Si estaba libre, ahora se usa
                } else {
                    eq.markAsAvailable(); // Si se usaba, ahora está libre
                }
                
                // IMPORTANTE: eq.markAs...() ya llama a save() internamente, 
                // así que el JSON ya se actualizó.

                // 5. Actualizar la Vista (la lista visual)
                // Usamos .set para reemplazar la línea vieja con la nueva info
                listEquipmentModel.set(index, eq.toSimpleString());
                
                JOptionPane.showMessageDialog(this, "Estado actualizado a: " + eq.getStatus());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar estado: " + e.getMessage());
        }
    }
    
