    // Función para agregar equipos (reutilizable)
    private void addEquipment(String name, String description) {
       // ⬇⬇⬇ PASO 1: REVISAR SI YA EXISTE ⬇⬇⬇
        for (int i = 0; i < listEquipmentModel.size(); i++) {
            // Obtenemos el texto de la fila (ej: "ID: 1 | Camera EOS R5...")
            String itemEnLista = listEquipmentModel.get(i);
            
            // Si el nombre del equipo ya está en esa línea...
            if (itemEnLista.contains(name)) {
                // ...avisamos y nos salimos
                JOptionPane.showMessageDialog(this, "El equipo '" + name + "' ya ha sido asignado.");
                return; // <--- AQUÍ SE DETIENE Y NO GUARDA NADA
            }
        }
        // ⬆⬆⬆ -------------------------------- ⬆⬆⬆

        // PASO 2: Si el código llega aquí, es porque NO estaba repetido. Procedemos normal.
        Equipment eq = new Equipment(name, description);
        eq.save();
        listEquipmentModel.addElement(eq.toSimpleString());
        jTextField1.setText(name);
    }

    // Función para eliminar
    private void removeEquipment() {
        int index = listEquipment.getSelectedIndex();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un equipo para eliminar");
            return;
        }

        String selectedString = listEquipmentModel.get(index);
        
        try {
            // Extraer ID del string "ID: 1 | Nombre..."
            String idPart = selectedString.split("\\|")[0]; 
            int id = Integer.parseInt(idPart.replace("ID:", "").trim());

            int opt = JOptionPane.showConfirmDialog(this, "¿Eliminar equipo ID " + id + "?", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (opt == JOptionPane.YES_OPTION) {
                Equipment eq = Equipment.findById(id);
                if (eq != null) {
                    eq.delete(); // Borrar del sistema
                }
                listEquipmentModel.remove(index); // Borrar de la vista
                jTextField1.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al procesar ID: " + e.getMessage());
        }
    }

