    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        int customerId;
        
        try {
            
            customerId = Integer.parseInt(txtCustomerId.getText());
            
        } catch (NumberFormatException e) {JOptionPane.showMessageDialog(this, "El ID del cliente debe ser numerico");
        
            return;
        }

        String eventName = txtEventName.getText();
        Date eventDate = jdcEventDate.getDate();

        int eventTypeCode = cmbEventType.getSelectedIndex() + 1;

        String result = EventController.registerEvent(
                customerId,
                eventName,
                eventDate,
                eventTypeCode
        );

        if ("OK".equals(result)) {
            JOptionPane.showMessageDialog(this, "Evento registrado correctamente");
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, result, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

