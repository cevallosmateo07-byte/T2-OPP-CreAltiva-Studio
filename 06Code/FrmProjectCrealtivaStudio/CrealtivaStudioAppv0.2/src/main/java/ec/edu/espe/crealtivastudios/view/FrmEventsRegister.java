    private void clearForm() {
        
        txtCustomerId.setText("");
        txtEventName.setText("");
        jdcEventDate.setDate(null);
        cmbEventType.setSelectedIndex(0);
    
    }
    
    private void loadCustomersTable() {

        String[] columns = {
            "ID", "Nombre", "Telefono", "Email", "Direccion", "Eventos"
        };

        DefaultTableModel model = new DefaultTableModel(null, columns) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Customer c : CustomerController.getAllCustomers()) {
            Object[] row = {
                c.getId(),
                c.getName(),
                c.getPhone(),
                c.getEmail(),
                c.getAddress(),
                c.getEventCount()
            };
            model.addRow(row);
        }

        tblCustomers.setModel(model);
    }

    
